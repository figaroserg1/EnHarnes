#!/usr/bin/env bash
# =============================================================================
# structural-tests.sh — Проверка архитектурных ограничений (слоёвые зависимости)
# =============================================================================
#
# ЧТО ДЕЛАЕТ:
#   Запускает pytest-тесты из tools/structural-tests/test_layer_dependencies.py,
#   которые проверяют, что импорты между слоями (Types, Config, Repo, Service,
#   Runtime, UI, Providers) идут только в разрешённых направлениях.
#   Например: UI может импортировать из Service, но Service не может
#   импортировать из UI.
#
# ЗАЧЕМ НУЖЕН:
#   Без автоматической проверки архитектурных границ слои постепенно
#   «протекают» друг в друга, и проект превращается в big ball of mud.
#   Этот скрипт — автоматический «архитектурный полицейский», который
#   блокирует нарушения на уровне CI.
#
# ИСПОЛЬЗОВАНИЕ:
#   bash scripts/structural-tests.sh
#   или: make structural
# =============================================================================
set -euo pipefail

pytest tools/structural-tests/test_layer_dependencies.py
